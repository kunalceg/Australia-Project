										<p>This course is ideal for experienced traffic control / management operators working as or would like to work as a Traffic Management Designer in the Resources and Infrastructure Industry in NSW.  This may include selection, modification and design of the traffic control guidance scheme. </p>

<p><strong>Traffic Management Designer course</strong> provide theory, practical skills and assessment for topics related to the duties of a traffic management designer including</p>
<ul>
<li>Prepare a work zone traffic management plan (TMP) </li>
<li>Collect all required information about a given roadwork project to enable the preparation of a TGS/TMP </li>
<li>Design a TGS/TMP, based on risk assessment, statutory and regulatory requirements, standards, road authority requirements and project brief </li>
<li>Select and modify a TGS/TMP based on risk assessment, statutory and regulatory requirements, standards, road authority requirements and project brief </li>
<li>Draw up a TGS/TMP to recognise other graphical representations such as pedestrian movement plans, vehicle movement plans, and notification of authorities</li>
<li>Determine the recommended spacing between signs and traffic control devices in line with standards; measure width of trafficable surface; calculate edge clearances to barriers, cones and clearance to work personnel </li>
<li>Incorporate environmental management plans </li>
<li>Conduct an onsite check and inspection of the plan and to identify any unexpected hazards/risks </li>
<li>Identify approvals required; identify approval agencies, types of approvals for roadway access, railway reserve access, authority to install signs on roads, variation to standards </li>
<li>Understand speed, environment, type and class of vehicles, traffic density, sight lines, environmental condition, weather patterns and surface type </li>
<li>Monitor and interpret control systems to apply to the drawing, selection and design.</li> 
<li>Use approved methods and follow recognised local legislation </li>
<li>Use the site/location assessment, distinguish topographical landmarks and carry out authorised risk control </li>
<li>Collate traffic volume data, type and class of vehicles </li>
<li>Determine lane capacity, road network performance and lane closure restrictions </li>
<li>Identify impacts from any concurrent or adjacent works </li>
<li>Identify times and dates of any planned public events that may result in increased or redistributed traffic patterns </li>
<li>Identify affected stakeholders and type of notification required and prepare notifications </li>
<li>Calculate costings for TMP development and implementation </li>
</ul>
