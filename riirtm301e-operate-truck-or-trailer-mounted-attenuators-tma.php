									<div>
										<p>This course is aimed at employees working in the traffic control /management industry. </p>
<p>
Truck Mounted Attenuators are designed for additional protection of workers and plant items in short term static works or mobile works. They are utilised to protect worksites on or beside active roads against errant vehicle intrusion.  TMAs also protect the occupants of errant vehicles through cushioning an impact that otherwise would be absorbed by a works vehicle </p>
<p>
This course provides the skills and knowledge required to operate a truck mounted attenuator when carrying out traffic management activities in civil construction. This course includes guidelines to protect the safety of road workers by providing physical protection via the attenuator when road or lane closures are not reasonably practical. </p>
<p>
The course also covers planning works involving attenuators, examples of Traffic Guidance Scheme (TGS), industry standard approaches in the use of attenuators, and safety measures for deploying the Scorpion Device and Raising during works or completion of works. </p>
